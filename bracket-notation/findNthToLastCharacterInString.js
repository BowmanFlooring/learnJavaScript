// Bracket Notation To Find The Nth To Last Character In A Given String

// Example

var firstName = "Dakota";
var thirdToLastLetterOfFirstName = firstName[firstName.length - 3];
console.log(thirdToLastLetterOfFirstName);

/**
 * RESULT:
 * o
 */

