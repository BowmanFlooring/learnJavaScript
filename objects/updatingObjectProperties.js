// Updating Object Properties

// We can use dot notation to update object properties

// Example

var ourDog = {
    "name": "Bane",
    "legs": 4,
    "tails": 1,
    "friends": ["everything!"]
};

ourDog.name = "Bane Bug";
// We use the assignment operator to assign a new name to that property