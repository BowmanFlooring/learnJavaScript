// Add New Properties To An Object

// You can add new properties to an object using dot or bracket notation

// Example
var ourDog = {
    "name": "Bane",
    "legs": 4,
    "tails": 1,
    "friends": ["everything!"]
};

// Adding a property with dot notation
ourDog.bark = "bow-wow";

// Adding a property with bracket notation
ourDog["tricks"] = [
    "sit",
    "speak",
    "spin around",
    "heel",
    "come",
    "stay"
];