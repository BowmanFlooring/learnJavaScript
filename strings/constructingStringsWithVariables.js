// Constructing Strings With Variables

// Example

var ourName = "freeCodeCamp";
var ourStr = "Hello, our name is " + ourName + ", how are you?";
console.log(ourStr);

/**
 * RESULT:
 * Hello, our name is freeCodeCamp, how are you?
 */