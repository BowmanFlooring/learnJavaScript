// Quoting Strings With Single Quotes

// In JavaScript, we won't have to use the '\' character to escape the quotation
// marks because there are other methods to do so! 

// Example

var myStr = "<a href=\"http://www.example.com\" target=\"_blank\">Link</a>";

// Instead of using double quotes to wrap the above string, we could just as
// easily use single quotes, and then our double quotes within don't need to
// be prepended with that annoying '\'!

var myStr = '<a href="http://www.example.com" target="_blank">Link</a>';

// Additionally, backticks can be used to surround a string. If you decide to
// use backticks, then you can use double AND single quotes within the string!