// Storing Values with Assignment Operator

// There is a difference between declaring variable and assigning variables

var a; // Declaring a variable
var b = 2; // Assigning a variable (the '=' is the assignment operator)
// This is also called 'initializing' the variable
a = 8; // 'a' is already declared, so we can assign it a value simply now

// use console.log to check the code:
console.log(a);
console.log(b); 