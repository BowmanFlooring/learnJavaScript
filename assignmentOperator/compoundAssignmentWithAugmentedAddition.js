// Compound Assignment With Augmented Addition

// It's common to want to add a number to a variable

var a = 3;
var b = 17;
var c = 12;

// only modify code below this line

a = a + 12;
b = b + 9;
c = c + 7;

// This is such common practice that there's a shortcut

a += 12;
b += 9;
c += 7;

// All three of those are the same as lines 12-14

// ALSO: the same can be done for '*', '/', and '-'