//// Iterate With While Loops

// Loops allow you to run the same code multiple times

// A 'while' loop runs so long as a specified condition is true
// The loops stops once the condition is no longer true

var myArray = [];

var i = 0;
while (i < 5) {
    myArray.push(i);
    i++
}

console.log(myArray); // RSLT: [ 0, 1, 2, 3, 4 ]