// Nested Arrays

// Example

var ourArray = [["theUniverse", 42], ["everything", 101010]];
var myArray = [["Bulls", 23], ["White Sox", 45]];
console.log([ourArray, myArray]);

/**
 * RESULT:
[
  [ [ 'theUniverse', 42 ], [ 'everything', 101010 ] ],
  [ [ 'Bulls', 23 ], [ 'White Sox', 45 ] ]
]
 */
