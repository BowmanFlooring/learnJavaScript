// Shopping List - Another Example Of Nested Arrays

// Here's our shopping list (an array), and in that array, another array!

var myList = [
    ["cereal", 3],
    ["milk", 2],
    ["bananas", 3],
    ["juice", 2],
    ["eggs", 12]
];

// Each array is a key-value pair - the key being the grocery item, 
// and the value being the quantity. Pretty easy so far!